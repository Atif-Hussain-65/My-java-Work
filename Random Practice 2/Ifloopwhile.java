public class Ifloopwhile
{
    public static void main(String[] args)
    {
        int i, j;
        i = 10;
        if (i > 0 && i < 100)
        {
            j = 1;
            while (j <= 5)
            {
                System.out.println("well good night");
                j++;
            }
        }
    }

}
